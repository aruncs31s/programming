def sum(a, b):
    return a + b


def differece(a, b):
    return a - b


def division(a, b):
    return a / b


def multiplication(a, b):
    return a * b


print("Sum = ", sum(10, 20))
print("Difference  = ", differece(10, 20))
print("Division = ", division(10, 20))
print("Multiplication = ", multiplication(10, 20))
